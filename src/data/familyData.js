// Build family groups by surname
export function buildFamilyGroups() {
  const groups = {};
  
  Object.values(persons).forEach(person => {
    const surname = person.surname;
    if (!surname || surname.startsWith('#') || surname.startsWith('0')) return;
    if (!groups[surname]) groups[surname] = { name: surname, members: [], size: 0 };
    groups[surname].members.push(person.id);
    groups[surname].size++;
  });

  return Object.values(groups).sort((a, b) => b.size - a.size);
}

// Get immediate family structure for a person
export function getPersonFamily(personId) {
  const person = persons[personId];
  if (!person) return null;

  // Find marriage where this person is husband or wife
  const myMarriages = Object.entries(marriages).filter(([fid, m]) => 
    m.husband === personId || m.wife === personId
  );

  // Find my children
  const myChildren = [];
  myMarriages.forEach(([fid]) => {
    const children = familyChildren[fid] || [];
    children.forEach(cid => {
      if (!myChildren.includes(cid)) myChildren.push(cid);
    });
  });

  // Find my parents
  const myParents = [];
  Object.entries(familyChildren).forEach(([fid, children]) => {
    if (children.includes(personId)) {
      const m = marriages[fid];
      if (m) {
        if (m.husband) myParents.push(m.husband);
        if (m.wife) myParents.push(m.wife);
      }
    }
  });

  return {
    person,
    spouses: myMarriages.map(([fid, m]) => ({
      familyId: fid,
      spouse: persons[m.husband === personId ? m.wife : m.husband],
      children: (familyChildren[fid] || []).map(cid => persons[cid]).filter(Boolean)
    })),
    parents: myParents.map(id => persons[id]).filter(Boolean),
    children: myChildren.map(id => persons[id]).filter(Boolean)
  };
}